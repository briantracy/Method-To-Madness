#import "UIColor+CustomColors.h"

@implementation UIColor (CustomColors)


+ (instancetype)tileColor
{
    return [UIColor colorWithRed:1 green:102.0f/255 blue:102.0f/255 alpha:1];
}

+ (instancetype)focusTileColor
{
    return [UIColor redColor];
}

@end
